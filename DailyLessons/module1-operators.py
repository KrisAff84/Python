# Addition
print (2 + 3) 
# Subtraction 
print (5 - 4) 
# Multiplication
print (3 * 5)
# Standard division, produces float  
print (6 / 2)  
print (7 / 2) 
# Integer division - returns nearest whole number rounded down
print (6 // 2)   
print (7 // 2)
# Modulus division - remainder of division
print (6 % 2)  
print (7 % 2)
# Cannot divide any number by zero with any division operators

# Power Operator
print (2 ** 3)  # 2 to the power of 3 (2^3)
print (6 ** 10)  # 6 to the power of 10 (6^10)

