# Numeric Values

age = 28
new_age = age + 5
print(new_age)

age = age + 7
print(age)

# Shortcut
age += 5  
print(age)

age *= 2
print(age)

age -= 40
print(age)

age /= 5
print(age)

# String Concatenation
text = 'hokus' + 'pokus'
print(text)

# Can multiply string by integer
print('hokus' * 5)

# Have to be extra careful about data types
# The examples below print differently

print(23+5)
print('23' + '5')
