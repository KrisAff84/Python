# Can use len function
user_data = ('John', 'American', 1964)
print(len(user_data))

# Use in and not in operators
if 'American' in user_data:
    print('This person comes from the US!')

# For loops
for element in user_data:
    print(element)

# Can be added together
user_data = ('John', 'American', 1964) + ('teacher', 'male')
print(user_data)

# Multiplied
numbers = (0, 1) * 10
print(numbers)

# Lists typically used for many values of same data type
# Tuples usually used for values of different types but related

